int	ft_atoi(char *str)
{
	int	num;
	int	sign;

	num = 0;
	sign = 1;
	while ((*str == ' ') || (*str == '\n') || (*str == '\t')
		|| (*str == '\v') || (*str == '\f') || (*str == '\r'))
		str ++;
	if (*str == '-')
		sign = -1;
	if ((*str == '-' ) || (*str == '+'))
		str++;
	while (*str >= '0' && *str <= '9')
	{
		num = (num * 10) + (*str - '0');
		str ++;
	}
	return (num * sign);
}
